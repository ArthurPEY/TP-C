#include <stdio.h>

int main() {   
    int a = 16;
    int b = 3;

    printf("Variable a :%i \n",a);
    printf("Variable b :%i \n",b);

    printf("Opération arithmétiques :\n");

    printf("a+b :%i \n",a+b);
    printf("a-b :%i \n",a-b);
    printf("a*b :%i \n",a*b);
    printf("a/b :%i \n",a/b);
    printf("a%b :%i \n",a%b);

    printf("Opération logiques : \n");

    printf("!a :%i \n",!a);
    printf("a&&b :%i \n",a&&b);
    printf("a||b :%i \n",a||b);
    return 0;
}